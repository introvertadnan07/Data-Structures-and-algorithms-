#include <iostream>
using namespace std;

int main() {

// Increment Operation 

    int a = 10;

    int b = a++;

    int x = 12;

    int y = ++x;

    cout <<"b = " << b << endl;
    cout <<"a = " << a << endl;

    cout <<"y = " << y << endl;
    cout <<"x = " << x << endl;

// Decrement Operations

    int m = 10;

    int n = m--;

    int r = 12;
    int s = --r;

    cout <<"n = " << n << endl;
    cout <<"m = " << m << endl;

    cout <<"s = " << s << endl;
    cout <<"r = " << r << endl;
    

    return 0;
}